    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function showDetailCategory(Request $request, $id)
    {
        if($request->ajax()) {
            $category = Product::join('category', 'category.id', '=', 'product.category_id')
            ->select('*','category.description as category_description', 'product.description as product_description')
            ->where('product.category_id', $id)
            ->get();
            return Datatables::of($category, $id)
            ->make(true);
        }

        return view('FlowerCategory.category-detail');
    }

